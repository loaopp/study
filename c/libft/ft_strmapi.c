#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*p;
	size_t	i;

	if (0 == s || 0 == f)
		return (0);
	p = (char*)ft_calloc(ft_strlen(s) + 1, sizeof(char));
	if (0 == p)
		return (0);
	i = 0;
	while (s[i])
	{
		p[i] = f(i, s[i]);
		i++;
	}
	return (p);
}
